-- Utility

function SetGodMode(vehID, state)
	if state == false then
	CDominoManager_GetInstance():SendRegisteredEventToEntity(vehID, "CVehicle", "SetAsDestructable")
	GodMode = false
	else
	CDominoManager_GetInstance():SendRegisteredEventToEntity(vehID, "CVehicle", "SetAsIndestructable")
	GodMode = true
	end
end

function SetBulletproofTires(vehID, state)
	if state == false then
	CDominoManager_GetInstance():SendRegisteredEventToEntity(vehID, "CVehicle", "SetTyresDeflatable")
	BulletproofTires = false
	else
	CDominoManager_GetInstance():SendRegisteredEventToEntity(vehID, "CVehicle", "SetTyresUndeflatable")
	BulletproofTires = true
	end
end

function GetPlayerVehicle()
    if AutoSelect then
        local player = GetLocalPlayerEntityId()
        local vehicle = GetCurrentVehicleEntityId(player)

        if vehicle ~= nil and vehicle ~= GetInvalidEntityId() then
            return vehicle
        end

        return nil
    end

    if SavedVehicle ~= nil and SavedVehicle ~= GetInvalidEntityId() then
        return SavedVehicle
    end

    return nil
end

function IsPlayerInVehicle()
    return GetPlayerVehicle() ~= nil
end

function ToggleAutoSelect(menu, text, hint, idx, enabled)
    local oldVehicle = GetPlayerVehicle()

    if oldVehicle then
        StopEntityHighlight(oldVehicle)
        SetGodMode(oldVehicle, false)
        SetBulletproofTires(oldVehicle, false)
    end

    AutoSelect = not AutoSelect

    local newVehicle = GetPlayerVehicle()

    if newVehicle and newVehicle ~= oldVehicle then
        StopEntityHighlight(newVehicle)
        SetGodMode(newVehicle, false)
        SetBulletproofTires(newVehicle, false)
    end
end

