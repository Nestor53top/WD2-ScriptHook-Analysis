function OpenTeleportCategory(title, tbl)
	local menu = UI.SimpleMenu()
	local hasSearch = tbl.search or false
	menu:SetTitle(title)
	if hasSearch == true then
	menu:AddSearch("Search", "Start typing to filter")
	end
	for _, v in pairs(tbl.spots) do
		local name, description, pos = unpack(v)
		menu:AddButton(name, description, function(menu, text, hint, index)
			ScriptHook.ShowNotification("Teleported to " .. text)
			ScriptHook.Teleport(pos[1], pos[2], pos[3])
		end)
	end	

	return menu
end

function WorldTeleportMenu()
	local menu = UI.SimpleMenu()
	menu:SetTitle("Teleport Menu")

	for _, v in ipairs(TeleportSpots) do
	menu:AddButton(v.name, v.desc, Script():CacheMenu(function() return OpenTeleportCategory(v.name, v) end))
	end

	return menu
end

